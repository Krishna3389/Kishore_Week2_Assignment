package assignment2;

public class Addition {
 
	    int a, b;
	    public Addition(int x, int y) {
	        a = x;
	        b = y;
	    }

	    public int add() {
	        return a + b;
	    }

	    public double add(double x, double y) {
	        return x + y;
	    }

	    public int add(int x, int y, int z) {
	        return x + y + z;
	    }
}
